package com.example.demo.form;

public class Form {
	
	private String name;
	private String comment;
	
	public Form() {}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getComment() {
			return comment;
	}	
	public void setComment(String comment) {
		this.comment = comment;
	}
	
}

